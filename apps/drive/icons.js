/** Create a Lucide SVG element. */
export function icon(name, { size = 16, className = "", filled = false } = {}) {
  const markup = ICONS[name];
  if (!markup) return document.createDocumentFragment();

  const svg = document.createElementNS("http://www.w3.org/2000/svg", "svg");
  svg.setAttribute("xmlns", "http://www.w3.org/2000/svg");
  svg.setAttribute("width", String(size));
  svg.setAttribute("height", String(size));
  svg.setAttribute("viewBox", "0 0 24 24");
  svg.setAttribute("fill", filled ? "currentColor" : "none");
  svg.setAttribute("stroke", "currentColor");
  svg.setAttribute("stroke-width", "2");
  svg.setAttribute("stroke-linecap", "round");
  svg.setAttribute("stroke-linejoin", "round");
  svg.setAttribute("aria-hidden", "true");
  if (className) svg.setAttribute("class", className);
  svg.innerHTML = markup;
  return svg;
}

/** Mount icons declared with data-icon attributes in static markup. */
export function mountDataIcons(root = document) {
  for (const el of root.querySelectorAll("[data-icon]")) {
    const name = el.dataset.icon;
    const size = Number(el.dataset.iconSize ?? 15);
    const filled = el.dataset.iconFilled === "true";
    el.replaceChildren(icon(name, { size, filled }));
  }
}
