/**
 * Generates typed I18nKey enum and per-locale JSON bundles from translation.json.
 * Run via `npm run make-i18n` before dev, build, and typecheck.
 */
const fs = require("fs");
const path = require("path");
const i18n = require("../src/i18n/translation.json");

const namespace = "arco";

const translationMap = {};

Object.entries(i18n).forEach(([key, transMap]) => {
  Object.entries(transMap).forEach(([lang, content]) => {
    if (!translationMap[lang]) {
      translationMap[lang] = {};
    }
    translationMap[lang][key] = content;
  });
});

const localesPath = path.join(__dirname, "../public/locales");
if (fs.existsSync(localesPath)) {
  fs.rmSync(localesPath, { recursive: true });
}

Object.entries(translationMap).forEach(([lang, transMap]) => {
  const filePath = path.join(localesPath, lang, `${namespace}.json`);
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${JSON.stringify(transMap, null, 2)}\n`);
});

const transKeys = Object.keys(translationMap.en ?? {});
const transKeyDeclareFilePath = path.join(__dirname, "../src/i18n/declaration.ts");
fs.mkdirSync(path.dirname(transKeyDeclareFilePath), { recursive: true });
fs.writeFileSync(
  transKeyDeclareFilePath,
  `// Generated by scripts/make-i18n-translations.cjs — do not edit manually.
export enum I18nKey {
${transKeys.map((key) => `  ${key} = "${key}",`).join("\n")}
}
`,
);

console.log(`Generated ${transKeys.length} keys across ${Object.keys(translationMap).length} locales.`);
